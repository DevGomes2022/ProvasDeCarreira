const btnA = document.getElementById("btn-resolucao-a");
    const infoA = document.getElementById("infoA");
    btnA.addEventListener("click", () => {
    if (infoA.style.display === "block") {
        infoA.style.display = "none";
    } else {
        infoA.style.display = "block";
    }
});

